// POINT: 変更前（プラットフォームスレッドプール）
// Tomcat: 最大200スレッド → 200同時リクエスト上限
// リクエストが DB 待ちで10秒ブロック → スレッド枯渇

// POINT: 変更後（仮想スレッド）
// 各リクエストが独自の仮想スレッドで処理
// DB 待ち中に仮想スレッドがアンマウント → キャリアスレッド解放
// 数万の同時リクエストでもスレッド枯渇しない

// POINT: 注意点
// 1. ThreadLocal/MDC → 仮想スレッドでも動作するが、大量のスレッドで
// メモリ消費が増加する可能性。ScopedValue を検討。
// 2. synchronized ブロック内のブロッキング I/O → ピン留め問題
// → ReentrantLock に置き換え推奨
// 3. コネクションプール → 依然として必要
// 仮想スレッドが10万あっても、DB コネクションは有限リソース
// HikariCP の maxPoolSize が実質的な並行 DB 操作数の上限